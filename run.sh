#!/bin/bash
# =========================================================
# БЛОК 1: Shebang — указывает, какой интерпретатор использовать
# =========================================================
# #!/bin/bash  — это не комментарий, а специальная инструкция для системы
# Она говорит: "Запускай этот скрипт с помощью интерпретатора bash"
# Без этой строки система не поймёт, чем запускать файл

# =========================================================
# БЛОК 2: Активация виртуального окружения
# =========================================================

source /home/niwelk/venv/bin/activate
# source — выполнить команды из указанного файла в текущем shell
# /home/niwelk/venv/bin/activate — это скрипт активации виртуального окружения
# 
# ЧТО ЭТО ДАЁТ:
# - после этой строки все команды python/pip будут использовать 
#   интерпретатор из /home/niwelk/venv/bin/python
# - становятся доступны все установленные пакеты (fastapi, streamlit, docker...)
#
# БЕЗ ЭТОЙ СТРОКИ:
# - команда uvicorn может не найтись (если не установлен глобально)
# - или будет использоваться системный Python без нужных библиотек

# =========================================================
# БЛОК 3: Запуск FastAPI (бэкенд) в фоновом режиме
# =========================================================

uvicorn main:app --reload --port 8000 &
# uvicorn — это сервер для запуска FastAPI приложений
# main:app — означает "в файле main.py возьми переменную app (экземпляр FastAPI)"
# --reload — автоматически перезапускать сервер при изменении кода (удобно для разработки)
# --port 8000 — запустить на порту 8000 (http://localhost:8000)
# & — амперсанд в конце означает "запустить в фоновом режиме"
#
# ЧТО ЭТО ДАЁТ:
# - бэкенд запускается и продолжает работать
# - терминал не блокируется (можно дальше писать команды)
# - скрипт идёт дальше, не дожидаясь остановки uvicorn

BACKEND_PID=$!
# $! — специальная переменная bash, которая содержит PID (Process ID)
# последнего запущенного в фоне процесса (в данном случае uvicorn)
# 
# ЧТО ЭТО ДАЁТ:
# - мы сохраняем номер процесса в переменную BACKEND_PID
# - потом сможем его остановить (например, по Ctrl+C)

# =========================================================
# БЛОК 4: Запуск Streamlit (фронтенд) в фоновом режиме
# =========================================================

streamlit run app.py --server.headless=true &
# streamlit run — запустить Streamlit приложение
# app.py — файл с интерфейсом
# --server.headless=true — отключить интерактивный режим (не спрашивать email при запуске)
# & — снова запуск в фоне
#
# ЧТО ЭТО ДАЁТ:
# - интерфейс становится доступен по адресу http://localhost:8501 (или 8502, если занято)
# - не блокирует терминал

FRONTEND_PID=$!
# Сохраняем PID процесса Streamlit

# =========================================================
# БЛОК 5: Ожидание и обработка сигналов
# =========================================================

wait
# wait — команда bash, которая ждёт завершения ВСЕХ фоновых процессов
#
# ЧТО ЭТО ДАЁТ:
# - скрипт не завершается сразу, а висит и ждёт
# - если нажать Ctrl+C, сигнал дойдёт до всех дочерних процессов
#
# БЕЗ ЭТОЙ СТРОКИ:
# - скрипт запустил бы uvicorn и streamlit и сразу завершился
# - вместе со скриптом завершились бы и все фоновые процессы (они бы упали)
